package School_Projects.Assignments.Variables_Assignmnets;

public class Assignment_13_What_Happens {
    public static void main(String[]args){
        int T1 = 21;
        int T2 = 12;
        int sum = T1 / T2;

        System.out.println(sum);

        byte B1 = 21;
        byte B2 = -12;
        byte sumB = (byte) (B1 / B2);

        System.out.println(sumB);

        short S1 = 21;
        short S2 = 12;
        short sumS = (short) (S1 / S2);

        System.out.println(sumS);

        long L1 = 21;
        long L2 = 12;
        long sumL = L1 / L2;

        System.out.println(sumL);

        float F1 = 21;
        float F2 = 12;
        float sumF = F1 / F2;

        System.out.println(sumF);

        double D1 = 21;
        double D2 = 12;
        double sumD = D1 / D2;

        System.out.println(sumD);
    }
}
